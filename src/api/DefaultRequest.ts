import axios from 'axios'

export const DEFAULT_URL = 'https://5eff-2001-fb1-23-dafb-adb8-4971-bad0-e38f.ngrok-free.app' + '/api'

export const RequestWithToken = (accessToken: string) => {
  return axios.create({
    baseURL: DEFAULT_URL,
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${accessToken}`
    }
  })
}

export const RequestWithoutToken = () => {
  return axios.create({
    baseURL: DEFAULT_URL,
    headers: {
      'Content-Type': 'application/json'
    }
  })
}

export const UploadRequestWithToken = (accessToken: string) => {
  return axios.create({
    baseURL: DEFAULT_URL,
    headers: {
      Authorization: `Bearer ${accessToken}`
    }
  })
}
